

def addition(a, b):
    """ This Function adds of two  given numbers """
    return a + b
def subtraction(a, b):
    """ This Function subtracts of two  given numbers """
    return a - b
def multiplication(a, b):
    """ This Function multiplications of two  given numbers """
    return a * b
def division(a, b):
    """ This Function divides of two  given numbers """
    return a / b

